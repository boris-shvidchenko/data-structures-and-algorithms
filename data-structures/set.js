function singleNumber(arr, index = 0) {
    while (true) {
        if (arr.filter(i => i === arr[index]).length === 1) return arr[index];
        index++;
    }
}

console.log(singleNumber([2,2,1])); // 1
console.log(singleNumber([4,1,2,1,2])); // 4
console.log(singleNumber([1])); //1 => THIS


