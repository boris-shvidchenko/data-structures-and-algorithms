const arr = [70,9,0,-200,40.5,4.4,20000,10,-2030];

const quickSort = (list) => {
    if (list.length <= 1) return list;
    const piv = list[0];
    const left = [];
    const right = [];
    for (let i = 1; i < list.length; i++) {
        if (list[i] < piv) {
            left.push(list[i]);
        } else {
            right.push(list[i]);
        }
    }
    return [...quickSort(left), piv, ...quickSort(right)];
}

