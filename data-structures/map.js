function fib(v) {
    if (v <= 2) return 1;
    const tab = new Array(v+1).fill(0);
    tab[1] = 1;
    for (let i = 0; i < v; i++) {
        tab[i+1] += tab[i];
        tab[i+2] += tab[i];
    }
    return tab[v];
}

console.log(fib(10));
console.log(fib(100));

function ssort(list, res=[]) {
    if (list.length === 0) return res;
    const min = Math.min(...list);
    const minIndex = list.indexOf(min);
    res.push(min);
    list.splice(minIndex, 1);
    return ssort(list, res);
}

console.log(ssort(a));